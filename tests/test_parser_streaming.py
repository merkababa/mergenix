class TestLineIterator:
    """Test _line_iterator with various input types."""

    def test_str_path(self):
        """_line_iterator should yield lines from a string file path."""
        lines = list(_line_iterator(str(SAMPLE_23ANDME)))
        assert len(lines) > 0
        # First line should be a 23andMe comment
        assert lines[0].startswith("# This data file generated by 23andMe")

    def test_path_object(self):
        """_line_iterator should yield lines from a Path object."""
        lines = list(_line_iterator(SAMPLE_23ANDME))
        assert len(lines) > 0
        assert lines[0].startswith("#")

    def test_bytesio(self):
        """_line_iterator should yield lines from a BytesIO object."""
        content = SAMPLE_23ANDME.read_text(encoding="utf-8")
        bio = BytesIO(content.encode("utf-8"))
        lines = list(_line_iterator(bio))
        assert len(lines) > 0
        assert lines[0].startswith("#")

    def test_stringio(self):
        """_line_iterator should yield lines from a StringIO (generic file-like)."""
        content = SAMPLE_ANCESTRY.read_text(encoding="utf-8")
        sio = StringIO(content)
        lines = list(_line_iterator(sio))
        assert len(lines) > 0
        assert lines[0].startswith("#")

    def test_lines_are_stripped_of_newlines(self):
        """Yielded lines should have trailing \\n and \\r removed."""
        content = "line1\nline2\r\nline3\n"
        bio = BytesIO(content.encode("utf-8"))
        lines = list(_line_iterator(bio))
        for line in lines:
            assert not line.endswith("\n")
            assert not line.endswith("\r")

    def test_empty_file(self):
        """_line_iterator on an empty file should yield nothing."""
        bio = BytesIO(b"")
        lines = list(_line_iterator(bio))
        assert lines == []


