class TestValidateFileSize:
    """Test _validate_file_size."""

    def test_accepts_small_file_path(self):
        """Should not raise for a small sample file."""
        _validate_file_size(str(SAMPLE_23ANDME))  # No exception expected

    def test_accepts_small_bytesio(self):
        """Should not raise for a small BytesIO."""
        bio = BytesIO(b"small content")
        _validate_file_size(bio)  # No exception expected

    def test_rejects_oversized_file_path(self, tmp_path):
        """Should raise ValueError for a file exceeding MAX_FILE_SIZE_BYTES."""
        # Create a sparse file (just set size metadata)
        big_file = tmp_path / "big.txt"
        big_file.write_bytes(b"x")
        # Patch Path.stat to report a huge size
        original_stat = Path.stat

        def fake_stat(self_path):
            if str(self_path) == str(big_file):
                result = original_stat(self_path)
                # Create a mock-like object with overridden st_size
                class FakeStat:
                    pass
                fs = FakeStat()
                for attr in dir(result):
                    if attr.startswith("st_"):
                        setattr(fs, attr, getattr(result, attr))
                fs.st_size = MAX_FILE_SIZE_BYTES + 1
                return fs
            return original_stat(self_path)

        with patch.object(Path, "stat", fake_stat):
            with pytest.raises(ValueError, match="File too large"):
                _validate_file_size(str(big_file))

    def test_rejects_oversized_bytesio(self):
        """Should raise ValueError for a BytesIO exceeding MAX_FILE_SIZE_BYTES."""

        class FakeBigBytesIO(BytesIO):
            """BytesIO subclass that reports an oversized getvalue()."""

            def getvalue(self):
                return b"x" * (MAX_FILE_SIZE_BYTES + 1)

        big_bio = FakeBigBytesIO(b"small")
        with pytest.raises(ValueError, match="File too large"):
            _validate_file_size(big_bio)

    def test_accepts_generic_file_object(self):
        """Generic file-like objects without getvalue/stat should pass silently."""
        sio = StringIO("hello")
        _validate_file_size(sio)  # Should not raise


