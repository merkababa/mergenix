class TestEmptyAndMalformedFiles:
    """Streaming parsers should raise clear errors for empty or malformed files."""

    def test_empty_23andme_raises(self):
        """Empty 23andMe file should raise ValueError."""
        bio = BytesIO(b"# 23andMe comment\n")
        with pytest.raises(ValueError, match="No valid SNP data"):
            _parse_23andme_streaming(bio)

    def test_empty_ancestry_raises(self):
        """Empty AncestryDNA file should raise ValueError."""
        bio = BytesIO(b"#AncestryDNA\nrsid\tchromosome\tposition\tallele1\tallele2\n")
        with pytest.raises(ValueError, match="No valid SNP data"):
            _parse_ancestry_streaming(bio)

    def test_empty_myheritage_raises(self):
        """Empty MyHeritage file should raise ValueError."""
        bio = BytesIO(b"RSID,CHROMOSOME,POSITION,RESULT\n")
        with pytest.raises(ValueError, match="No valid SNP data"):
            _parse_myheritage_streaming(bio)

    def test_empty_vcf_raises(self):
        """Empty VCF file should raise ValueError."""
        content = (
            "##fileformat=VCFv4.2\n"
            "#CHROM\tPOS\tID\tREF\tALT\tQUAL\tFILTER\tINFO\tFORMAT\tSAMPLE\n"
        )
        bio = BytesIO(content.encode("utf-8"))
        with pytest.raises(ValueError, match="No valid SNP data"):
            _parse_vcf_streaming(bio)

    def test_completely_empty_file(self):
        """A zero-byte file should produce no lines and fail gracefully."""
        bio = BytesIO(b"")
        with pytest.raises(ValueError, match="No valid SNP data"):
            _parse_23andme_streaming(bio)


