class TestSNPCountLimit:
    """Streaming parsers should raise when SNP count exceeds MAX_SNP_COUNT."""

    def test_23andme_snp_limit(self, tmp_path):
        """23andMe streaming parser should raise on exceeding SNP count limit."""
        # Generate a file with more than MAX_SNP_COUNT lines
        # We use a small limit via patching to avoid generating 10M lines
        big_file = tmp_path / "big_23andme.txt"
        lines = ["# This data file generated by 23andMe"]
        for i in range(110):
            lines.append(f"rs{i}\t1\t{1000 + i}\tAA")
        big_file.write_text("\n".join(lines), encoding="utf-8")

        with patch("Source.parser.MAX_SNP_COUNT", 100):
            with pytest.raises(ValueError, match="SNP count exceeds maximum"):
                _parse_23andme_streaming(str(big_file))

    def test_ancestry_snp_limit(self, tmp_path):
        """AncestryDNA streaming parser should raise on exceeding SNP count limit."""
        big_file = tmp_path / "big_ancestry.txt"
        lines = ["#AncestryDNA", "rsid\tchromosome\tposition\tallele1\tallele2"]
        for i in range(110):
            lines.append(f"rs{i}\t1\t{1000 + i}\tA\tG")
        big_file.write_text("\n".join(lines), encoding="utf-8")

        with patch("Source.parser.MAX_SNP_COUNT", 100):
            with pytest.raises(ValueError, match="SNP count exceeds maximum"):
                _parse_ancestry_streaming(str(big_file))

    def test_myheritage_snp_limit(self, tmp_path):
        """MyHeritage streaming parser should raise on exceeding SNP count limit."""
        big_file = tmp_path / "big_myheritage.csv"
        lines = ["RSID,CHROMOSOME,POSITION,RESULT"]
        for i in range(110):
            lines.append(f"rs{i},1,{1000 + i},AG")
        big_file.write_text("\n".join(lines), encoding="utf-8")

        with patch("Source.parser.MAX_SNP_COUNT", 100):
            with pytest.raises(ValueError, match="SNP count exceeds maximum"):
                _parse_myheritage_streaming(str(big_file))

    def test_vcf_snp_limit(self, tmp_path):
        """VCF streaming parser should raise on exceeding SNP count limit."""
        big_file = tmp_path / "big.vcf"
        lines = [
            "##fileformat=VCFv4.2",
            "#CHROM\tPOS\tID\tREF\tALT\tQUAL\tFILTER\tINFO\tFORMAT\tSAMPLE",
        ]
        for i in range(110):
            lines.append(f"1\t{1000 + i}\trs{i}\tA\tG\t.\tPASS\tNS=1\tGT\t0/1")
        big_file.write_text("\n".join(lines), encoding="utf-8")

        with patch("Source.parser.MAX_SNP_COUNT", 100):
            with pytest.raises(ValueError, match="SNP count exceeds maximum"):
                _parse_vcf_streaming(str(big_file))


