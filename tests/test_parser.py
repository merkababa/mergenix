    def test_mixed_line_endings(self, valid_23andme_content):
        """Parser should handle mixed line endings (\\n, \\r\\n)."""
        # Replace some newlines with \r\n
        content = valid_23andme_content.replace("\n", "\r\n", 3)
        bio = BytesIO(content.encode("utf-8"))
