class TestParse23andMe:
    """Test parse_23andme and validation."""

    def test_parse_23andme_from_path(self):
        """parse_23andme should parse a real sample file from disk."""
        snps = parse_23andme(str(SAMPLE_23ANDME))
        assert isinstance(snps, dict)
        assert len(snps) > 0
        # Check known rsid from the sample
        assert "rs75030207" in snps

    def test_parse_23andme_from_bytesio(self):
        """parse_23andme should parse from BytesIO."""
        content = SAMPLE_23ANDME.read_text(encoding="utf-8")
        bio = BytesIO(content.encode("utf-8"))
        snps = parse_23andme(bio)
        assert isinstance(snps, dict)
        assert len(snps) > 0

    def test_parse_23andme_from_stringio(self, valid_23andme_content):
        """parse_23andme should parse from StringIO."""
        sio = StringIO(valid_23andme_content)
        snps = parse_23andme(sio)
        assert len(snps) == 11

    def test_parse_23andme_genotype_values(self, valid_23andme_content):
        """Genotype values should be correct for known rsids."""
        bio = BytesIO(valid_23andme_content.encode("utf-8"))
        snps = parse_23andme(bio)
        assert snps["rs4477212"] == "AA"
        assert snps["rs3094315"] == "AG"
        assert snps["rs1110052"] == "GT"

    def test_parse_23andme_skips_no_calls(self):
        """No-call entries (genotype '--') should be skipped."""
        content = (
            "# 23andMe data\n"
            "# rsid\tchromosome\tposition\tgenotype\n"
            "rs4477212\t1\t82154\tAA\n"
            "rs3094315\t1\t752566\t--\n"
            "rs3131972\t1\t752721\tGG\n"
        )
        bio = BytesIO(content.encode("utf-8"))
        snps = parse_23andme(bio)
        assert "rs3094315" not in snps
        assert len(snps) == 2

    def test_parse_23andme_skips_non_rs_header_rows(self):
        """Uncommented header rows starting with 'SNP' or 'ID' should be skipped.

        Note: 'rsid' starts with 'rs' so the parser treats it as a valid ID;
        only header keywords that do NOT start with 'rs'/'i' are skipped.
        """
        content = (
            "# 23andMe data\n"
            "# rsid\tchromosome\tposition\tgenotype\n"
            "SNP\tchromosome\tposition\tgenotype\n"
            "rs4477212\t1\t82154\tAA\n"
            "rs3094315\t1\t752566\tAG\n"
        )
        bio = BytesIO(content.encode("utf-8"))
        snps = parse_23andme(bio)
        assert "SNP" not in snps
        assert len(snps) == 2

    def test_parse_23andme_file_not_found(self, tmp_path):
        """parse_23andme should raise FileNotFoundError for missing files."""
        with pytest.raises(FileNotFoundError):
            parse_23andme(str(tmp_path / "nonexistent.txt"))

    def test_parse_23andme_invalid_format_raises(self):
        """parse_23andme should raise ValueError for invalid format."""
        content = "just some random text\nno valid data here\n"
        bio = BytesIO(content.encode("utf-8"))
        with pytest.raises(ValueError, match="Invalid 23andMe"):
            parse_23andme(bio)

    def test_parse_23andme_empty_data_raises(self):
        """parse_23andme should raise ValueError when no valid SNPs found."""
        content = (
            "# 23andMe data\n"
            "# rsid\tchromosome\tposition\tgenotype\n"
            "rs4477212\t1\t82154\t--\n"
            "rs3094315\t1\t752566\t--\n"
        )
        bio = BytesIO(content.encode("utf-8"))
        with pytest.raises(ValueError, match="No valid SNP data"):
            parse_23andme(bio)

    def test_parse_23andme_v4_sample(self):
        """parse_23andme should parse a v4 sample file."""
        snps = parse_23andme(str(SAMPLE_23ANDME_V4))
        assert isinstance(snps, dict)
        assert len(snps) > 0

    def test_parse_23andme_v5_sample(self):
        """parse_23andme should parse a v5 sample file."""
        snps = parse_23andme(str(SAMPLE_23ANDME_V5))
        assert isinstance(snps, dict)
        assert len(snps) > 0

    def test_parse_23andme_accepts_indel_ids(self, valid_23andme_content):
        """Parser should accept 'i'-prefixed IDs (internal/indel markers)."""
        content = valid_23andme_content + "i4000759\t1\t900000\tDI\n"
        bio = BytesIO(content.encode("utf-8"))
        snps = parse_23andme(bio)
        assert "i4000759" in snps
        assert snps["i4000759"] == "DI"


