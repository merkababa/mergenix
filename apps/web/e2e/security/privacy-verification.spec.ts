/**
 * Privacy Verification E2E Tests
 *
 * Verifies Mergenix's core privacy promise: "Your DNA never leaves your device."
 *
 * All genetic analysis runs client-side in Web Workers. These tests use
 * Playwright's network interception layer to prove that NO genetic data
 * is ever transmitted to any server during the analysis workflow.
 *
 * Strategy: Allowlist mode
 *   - Before analysis, install a page.on('request') listener that captures
 *     every outgoing network request.
 *   - After analysis completes, assert that zero captured requests contain
 *     genetic data (rsIDs, genotype strings, large POST bodies).
 *   - Assert that zero requests were made to third-party tracking domains.
 *
 * 5 test scenarios — all P0 (privacy is a core product guarantee).
 *
 * @see apps/web/hooks/use-genetics-worker.ts  — Web Worker bridge
 * @see packages/genetics-engine/src/worker    — Worker implementation
 */

