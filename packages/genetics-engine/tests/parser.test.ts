// ─── VCF Parser ─────────────────────────────────────────────────────────────

describe('parseVcf', () => {
  it('should parse valid VCF content', () => {
    const result = parseVcf(SAMPLE_VCF);
    expect(result['rs4477212']).toBe('AA');
    expect(result['rs3094315']).toBe('AG');
    expect(result['rs3131972']).toBe('GG');
    expect(result['rs12124819']).toBe('CC');
    expect(Object.keys(result)).toHaveLength(4);
  });

  it('should correctly map GT allele indices to nucleotides', () => {
    const content = [
      '##fileformat=VCFv4.1',
      '#CHROM\tPOS\tID\tREF\tALT\tQUAL\tFILTER\tINFO\tFORMAT\tSAMPLE',
      '1\t100\trs100\tA\tG\t30\tPASS\t.\tGT\t0/0',    // AA (ref/ref)
      '1\t200\trs200\tA\tG\t30\tPASS\t.\tGT\t0/1',    // AG (ref/alt)
      '1\t300\trs300\tA\tG\t30\tPASS\t.\tGT\t1/1',    // GG (alt/alt)
    ].join('\n');
    const result = parseVcf(content);
    expect(result['rs100']).toBe('AA');
    expect(result['rs200']).toBe('AG');
    expect(result['rs300']).toBe('GG');
  });

  it('should skip indels (multi-base REF or ALT)', () => {
    const content = [
      '##fileformat=VCFv4.1',
      '#CHROM\tPOS\tID\tREF\tALT\tQUAL\tFILTER\tINFO\tFORMAT\tSAMPLE',
      '1\t100\trs100\tA\tG\t30\tPASS\t.\tGT\t0/1',       // SNP - kept
      '1\t200\trs200\tAT\tG\t30\tPASS\t.\tGT\t0/1',      // Indel REF - skipped
      '1\t300\trs300\tA\tGTT\t30\tPASS\t.\tGT\t0/1',     // Indel ALT - skipped
    ].join('\n');
    const result = parseVcf(content);
    expect(Object.keys(result)).toHaveLength(1);
    expect(result['rs100']).toBe('AG');
  });

  it('should skip no-call genotypes (./.)', () => {
    const content = [
      '##fileformat=VCFv4.1',
      '#CHROM\tPOS\tID\tREF\tALT\tQUAL\tFILTER\tINFO\tFORMAT\tSAMPLE',
      '1\t100\trs100\tA\tG\t30\tPASS\t.\tGT\t0/1',
      '1\t200\trs200\tA\tG\t30\tPASS\t.\tGT\t./.',
      '1\t300\trs300\tA\tG\t30\tPASS\t.\tGT\t.|.',
    ].join('\n');
    const result = parseVcf(content);
    expect(Object.keys(result)).toHaveLength(1);
    expect(result['rs100']).toBe('AG');
  });

  it('should only include variants with rsIDs', () => {
    const content = [
      '##fileformat=VCFv4.1',
      '#CHROM\tPOS\tID\tREF\tALT\tQUAL\tFILTER\tINFO\tFORMAT\tSAMPLE',
      '1\t100\trs100\tA\tG\t30\tPASS\t.\tGT\t0/1',
      '1\t200\t.\tA\tG\t30\tPASS\t.\tGT\t0/1',      // "." ID - skipped
      '1\t300\tCHR1_300\tA\tG\t30\tPASS\t.\tGT\t0/1', // No rs prefix - skipped
    ].join('\n');
    const result = parseVcf(content);
    expect(Object.keys(result)).toHaveLength(1);
    expect(result['rs100']).toBe('AG');
  });

  it('should handle phased (|) and unphased (/) GT separators', () => {
    const content = [
      '##fileformat=VCFv4.1',
      '#CHROM\tPOS\tID\tREF\tALT\tQUAL\tFILTER\tINFO\tFORMAT\tSAMPLE',
      '1\t100\trs100\tA\tG\t30\tPASS\t.\tGT\t0/1',   // unphased
      '1\t200\trs200\tA\tG\t30\tPASS\t.\tGT\t1|0',   // phased
    ].join('\n');
    const result = parseVcf(content);
    expect(result['rs100']).toBe('AG');
    expect(result['rs200']).toBe('GA');
  });

  it('should throw on empty VCF data', () => {
    const content = [
      '##fileformat=VCFv4.1',
      '#CHROM\tPOS\tID\tREF\tALT\tQUAL\tFILTER\tINFO\tFORMAT\tSAMPLE',
    ].join('\n');
    expect(() => parseVcf(content)).toThrow('No valid SNP data found');
  });

  it('should handle multi-allelic ALT correctly', () => {
    const content = [
      '##fileformat=VCFv4.1',
      '#CHROM\tPOS\tID\tREF\tALT\tQUAL\tFILTER\tINFO\tFORMAT\tSAMPLE',
      '1\t100\trs100\tA\tG,C\t30\tPASS\t.\tGT\t1/2',  // G and C alts
    ].join('\n');
    const result = parseVcf(content);
    expect(result['rs100']).toBe('GC');
  });

  it('should skip lines before the #CHROM header', () => {
    const content = [
      '##fileformat=VCFv4.1',
      '##contig=<ID=1>',
      'not_a_data_line_before_header',
      '#CHROM\tPOS\tID\tREF\tALT\tQUAL\tFILTER\tINFO\tFORMAT\tSAMPLE',
      '1\t100\trs100\tA\tG\t30\tPASS\t.\tGT\t0/1',
    ].join('\n');
    const result = parseVcf(content);
    expect(Object.keys(result)).toHaveLength(1);
  });
});

