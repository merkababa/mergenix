describe('buildParseResultSummary', () => {
  it('should compute skippedLines from rawLineCount', () => {
    const genotypes = { rs1: 'AA', rs2: 'AG', rs3: 'GG' };
    // 10 raw lines, 3 valid SNPs -> 7 skipped (comments, headers, blank, no-calls)
    const summary = buildParseResultSummary(genotypes, '23andme', {}, 10);
    expect(summary.totalSnps).toBe(3);
    expect(summary.validSnps).toBe(3);
    expect(summary.skippedLines).toBe(7);
    expect(summary.format).toBe('23andme');
  });

  it('should default skippedLines to 0 when rawLineCount is not provided', () => {
    const genotypes = { rs1: 'AA', rs2: 'AG' };
    const summary = buildParseResultSummary(genotypes, 'vcf');
    expect(summary.skippedLines).toBe(0);
  });

  it('should include fileName in metadata when provided', () => {
    const genotypes = { rs1: 'AA' };
    const summary = buildParseResultSummary(genotypes, 'ancestrydna', { fileName: 'test.txt' });
    expect(summary.metadata['fileName']).toBe('test.txt');
  });

  it('should handle empty genotype map', () => {
    const summary = buildParseResultSummary({}, 'unknown', {}, 5);
    expect(summary.totalSnps).toBe(0);
    expect(summary.skippedLines).toBe(5);
  });
});

