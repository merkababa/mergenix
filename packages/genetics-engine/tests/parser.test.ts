// ─── Format Detection ───────────────────────────────────────────────────────

describe('detectFormat', () => {
  it('should detect 23andMe format from comment signature', () => {
    expect(detectFormat(SAMPLE_23ANDME)).toBe('23andme');
  });

  it('should detect AncestryDNA format from comment signature', () => {
    expect(detectFormat(SAMPLE_ANCESTRYDNA)).toBe('ancestrydna');
  });

  it('should detect VCF format from ##fileformat=VCF line', () => {
    expect(detectFormat(SAMPLE_VCF)).toBe('vcf');
  });

  it('should detect MyHeritage format from CSV header', () => {
    expect(detectFormat(SAMPLE_MYHERITAGE)).toBe('myheritage');
  });

  it('should return "unknown" for unrecognized formats', () => {
    expect(detectFormat('random data\nno format here')).toBe('unknown');
    expect(detectFormat('')).toBe('unknown');
    expect(detectFormat('just some text')).toBe('unknown');
  });

  it('should detect 23andMe format from 4-column tab-separated data without header', () => {
    // 23andMe heuristic detection: 4-column tab-separated with rs-prefixed IDs
    const noHeader = [
      'rs4477212\t1\t82154\tAA',
      'rs3094315\t1\t752566\tAG',
      'rs3131972\t1\t752721\tGG',
      'rs12124819\t1\t776546\tAA',
    ].join('\n');
    expect(detectFormat(noHeader)).toBe('23andme');
  });

  it('should detect AncestryDNA format from 5-column header with allele1/allele2', () => {
    const headerOnly = [
      'rsid\tchromosome\tposition\tallele1\tallele2',
      'rs4477212\t1\t82154\tA\tA',
      'rs3094315\t1\t752566\tA\tG',
    ].join('\n');
    expect(detectFormat(headerOnly)).toBe('ancestrydna');
  });

  it('should detect VCF from ## comments with #CHROM header', () => {
    const vcfNoFileformat = [
      '##source=myApp',
      '#CHROM\tPOS\tID\tREF\tALT\tQUAL\tFILTER\tINFO\tFORMAT\tSAMPLE',
      '1\t100\trs123\tA\tG\t30\tPASS\t.\tGT\t0/1',
    ].join('\n');
    expect(detectFormat(vcfNoFileformat)).toBe('vcf');
  });

  it('should detect MyHeritage from data heuristics without explicit header', () => {
    // 4-column comma-separated with rs-prefixed IDs and 2-char results
    const noHeaderMyHeritage = [
      'rs4477212,1,82154,AA',
      'rs3094315,1,752566,AG',
      'rs3131972,1,752721,GG',
    ].join('\n');
    expect(detectFormat(noHeaderMyHeritage)).toBe('myheritage');
  });
});

// ─── 23andMe Parser ─────────────────────────────────────────────────────────

describe('parse23andMe', () => {
  it('should parse valid 23andMe content', () => {
    const result = parse23andMe(SAMPLE_23ANDME);
    expect(result['rs4477212']).toBe('AA');
    expect(result['rs3094315']).toBe('AG');
    expect(result['rs3131972']).toBe('GG');
    expect(result['rs12124819']).toBe('AA');
    expect(result['i713426']).toBe('CC');
    expect(Object.keys(result)).toHaveLength(5);
  });

  it('should skip comment lines starting with #', () => {
    const content = [
      '# Comment line 1',
      '# Comment line 2',
      'rs4477212\t1\t82154\tAA',
    ].join('\n');
    const result = parse23andMe(content);
    expect(Object.keys(result)).toHaveLength(1);
    expect(result['rs4477212']).toBe('AA');
  });

  it('should skip no-call entries (--)', () => {
    const content = [
      'rs4477212\t1\t82154\tAA',
      'rs3094315\t1\t752566\t--',
      'rs3131972\t1\t752721\tGG',
    ].join('\n');
    const result = parse23andMe(content);
    expect(Object.keys(result)).toHaveLength(2);
    expect(result['rs3094315']).toBeUndefined();
  });

  it('should skip malformed lines with fewer than 4 columns', () => {
    const content = [
      'rs4477212\t1\t82154\tAA',
      'rs3094315\t1',
      'only_one_column',
      'rs3131972\t1\t752721\tGG',
    ].join('\n');
    const result = parse23andMe(content);
    expect(Object.keys(result)).toHaveLength(2);
  });

  it('should throw on empty file', () => {
    expect(() => parse23andMe('')).toThrow('No valid SNP data found');
    expect(() => parse23andMe('# only comments\n# nothing here')).toThrow(
      'No valid SNP data found',
    );
  });

  it('should handle both rs and i prefixed rsIDs', () => {
    const content = [
      'rs4477212\t1\t82154\tAA',
      'i713426\t1\t817186\tCC',
    ].join('\n');
    const result = parse23andMe(content);
    expect(result['rs4477212']).toBe('AA');
    expect(result['i713426']).toBe('CC');
  });

  it('should skip all common header rows (rsid, id, snp)', () => {
    // All common header keywords are skipped before the rsid prefix check

    // "snp" header is skipped
    const contentWithSnp = [
      'snp\tchromosome\tposition\tgenotype',
      'rs4477212\t1\t82154\tAA',
    ].join('\n');
    const resultSnp = parse23andMe(contentWithSnp);
    expect(Object.keys(resultSnp)).toHaveLength(1);
    expect(resultSnp['rs4477212']).toBe('AA');

    // "rsid" header is skipped
    const contentWithRsid = [
      'rsid\tchromosome\tposition\tgenotype',
      'rs4477212\t1\t82154\tAA',
    ].join('\n');
    const resultRsid = parse23andMe(contentWithRsid);
    expect(Object.keys(resultRsid)).toHaveLength(1);
    expect(resultRsid['rs4477212']).toBe('AA');

    // "id" header is skipped
    const contentWithId = [
      'id\tchromosome\tposition\tgenotype',
      'rs4477212\t1\t82154\tAA',
    ].join('\n');
    const resultId = parse23andMe(contentWithId);
    expect(Object.keys(resultId)).toHaveLength(1);
    expect(resultId['rs4477212']).toBe('AA');
  });

  it('should skip empty genotype entries', () => {
    const content = [
      'rs4477212\t1\t82154\tAA',
      'rs3094315\t1\t752566\t',
      'rs3131972\t1\t752721\tGG',
    ].join('\n');
    const result = parse23andMe(content);
    expect(Object.keys(result)).toHaveLength(2);
  });
});

