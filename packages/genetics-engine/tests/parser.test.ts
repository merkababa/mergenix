// ─── Genotype Statistics ────────────────────────────────────────────────────

describe('getGenotypeStats', () => {
  it('should correctly count homozygous and heterozygous genotypes', () => {
    const genotypes = {
      rs1: 'AA',  // homozygous
      rs2: 'AG',  // heterozygous
      rs3: 'GG',  // homozygous
      rs4: 'CT',  // heterozygous
      rs5: 'TT',  // homozygous
    };
    const stats = getGenotypeStats(genotypes);
    expect(stats.totalSnps).toBe(5);
    expect(stats.homozygousCount).toBe(3);
    expect(stats.heterozygousCount).toBe(2);
  });

  it('should return zero counts for empty genotype map', () => {
    const stats = getGenotypeStats({});
    expect(stats.totalSnps).toBe(0);
    expect(stats.homozygousCount).toBe(0);
    expect(stats.heterozygousCount).toBe(0);
    expect(stats.genotypeDistribution).toEqual({});
  });

  it('should count single-character genotypes as homozygous (chrX/Y males)', () => {
    const genotypes = {
      rs1: 'A',   // single allele, hemi
      rs2: 'AG',  // heterozygous
    };
    const stats = getGenotypeStats(genotypes);
    expect(stats.homozygousCount).toBe(1);
    expect(stats.heterozygousCount).toBe(1);
  });

  it('should build a genotype distribution', () => {
    const genotypes = {
      rs1: 'AA',
      rs2: 'AA',
      rs3: 'AG',
      rs4: 'GG',
    };
    const stats = getGenotypeStats(genotypes);
    expect(stats.genotypeDistribution['AA']).toBe(2);
    expect(stats.genotypeDistribution['AG']).toBe(1);
    expect(stats.genotypeDistribution['GG']).toBe(1);
  });
});

