// ─── 1. Parser — Format Detection ────────────────────────────────────────────
//
// Tests format detection across header variations, heuristic fallback,
// 23andMe version-specific comment lines, and VCF alternatives.

describe('Parser — Format Detection', () => {
  // ── 23andMe version-specific comment headers ──────────────────────────────

  it('detects 23andMe v3 from comment containing "23andMe" + build 37', () => {
    // 23andMe v3 files (OmniExpress Plus) often include build number in comments.
    const content = [
      '# This data file generated by 23andMe',
      '# Below is a text version of your data.  Fields are TAB-separated',
      '# Build: 37',
      '# rsid\tchromosome\tposition\tgenotype',
      'rs4477212\t1\t82154\tAA',
    ].join('\n');
    expect(detectFormat(content)).toBe('23andme');
  });

  it('detects 23andMe v4 from comment containing "23andMe" and standard columns', () => {
    // v4 files have the same comment structure as v3.
    // Detection relies on the "23andme" keyword in the comment.
    const content = [
      '# This data file generated by 23andMe at: Sat May 01 2021',
      '# Genome Build: 37',
      '# rsid\tchromosome\tposition\tgenotype',
      'rs3094315\t1\t752566\tAG',
    ].join('\n');
    expect(detectFormat(content)).toBe('23andme');
  });

  it('detects 23andMe v5 from comment containing "23andMe" GSA chip note', () => {
    // v5 (GSA) files still have "23andMe" in the comment header.
    const content = [
      '# This data file was generated by 23andMe',
      '# Genome Build 37 SNP data - Illumina GSA chip',
      '# rsid\tchromosome\tposition\tgenotype',
      'rs548049170\t1\t100000\tAA',
    ].join('\n');
    expect(detectFormat(content)).toBe('23andme');
  });

  it('detects AncestryDNA v1 from comment "AncestryDNA" keyword', () => {
    // v1 (OmniExpress) files: comment with "AncestryDNA", 5-col tab format.
    const content = [
      '#AncestryDNA raw data download',
      'rsid\tchromosome\tposition\tallele1\tallele2',
      'rs4477212\t1\t82154\tA\tA',
    ].join('\n');
    expect(detectFormat(content)).toBe('ancestrydna');
  });

  it('detects AncestryDNA v2 from comment "AncestryDNA" keyword', () => {
    // v2 (GSA) files have the same comment structure.
    const content = [
      '#AncestryDNA',
      '#This file was generated by AncestryDNA at: 2022-01-01',
      'rsid\tchromosome\tposition\tallele1\tallele2',
      'rs548049170\t1\t100000\tA\tG',
    ].join('\n');
    expect(detectFormat(content)).toBe('ancestrydna');
  });

  it('detects AncestryDNA from 5-column tab header with allele1/allele2 (no keyword)', () => {
    // AncestryDNA heuristic: 5-column header with "allele1" and "allele2".
    const content = [
      'rsid\tchromosome\tposition\tallele1\tallele2',
      'rs4477212\t1\t82154\tA\tA',
      'rs3094315\t1\t752566\tA\tG',
    ].join('\n');
    expect(detectFormat(content)).toBe('ancestrydna');
  });

  it('detects MyHeritage from CSV header RSID,CHROMOSOME,POSITION,RESULT', () => {
    // MyHeritage: CSV with explicit column names.
    const content = ['RSID,CHROMOSOME,POSITION,RESULT', 'rs4477212,1,82154,AA'].join('\n');
    expect(detectFormat(content)).toBe('myheritage');
  });

  it('detects MyHeritage from lowercase CSV header (case-insensitive match)', () => {
    // The parser lowercases for comparison — lowercase header should still match.
    const content = ['rsid,chromosome,position,result', 'rs4477212,1,82154,AA'].join('\n');
    expect(detectFormat(content)).toBe('myheritage');
  });

  it('detects VCF from ##fileformat=VCFv4.2', () => {
    // Any VCF version number should trigger VCF detection.
    const content = [
      '##fileformat=VCFv4.2',
      '#CHROM\tPOS\tID\tREF\tALT\tQUAL\tFILTER\tINFO\tFORMAT\tS1',
      '1\t100\trs1\tA\tG\t.\tPASS\t.\tGT\t0/1',
    ].join('\n');
    expect(detectFormat(content)).toBe('vcf');
  });

  it('detects VCF from ##fileformat=VCFv4.3', () => {
    const content = [
      '##fileformat=VCFv4.3',
      '#CHROM\tPOS\tID\tREF\tALT\tQUAL\tFILTER\tINFO\tFORMAT\tS1',
      '1\t100\trs1\tA\tG\t.\tPASS\t.\tGT\t0/1',
    ].join('\n');
    expect(detectFormat(content)).toBe('vcf');
  });

  it('detects VCF from ## meta-lines + #CHROM (no ##fileformat line)', () => {
    // VCF heuristic: ## meta-line followed by #CHROM header.
    const content = [
      '##source=SomeApp',
      '##reference=GRCh37',
      '#CHROM\tPOS\tID\tREF\tALT\tQUAL\tFILTER\tINFO\tFORMAT\tS1',
      '1\t100\trs1\tA\tG\t.\tPASS\t.\tGT\t0/1',
    ].join('\n');
    expect(detectFormat(content)).toBe('vcf');
  });

  it('returns "unknown" for empty string', () => {
    expect(detectFormat('')).toBe('unknown');
  });

  it('returns "unknown" for whitespace-only content', () => {
    expect(detectFormat('   \n   \n   ')).toBe('unknown');
  });

  it('returns "unknown" for plain prose text', () => {
    expect(detectFormat('This is not a genetic data file. It contains only prose.')).toBe(
      'unknown',
    );
  });

  it('returns "unknown" for content with 4 columns but non-rs IDs (no clear format)', () => {
    // 4-column but IDs don't start with rs or i => not 23andMe heuristic
    const content = ['GENE001\t1\t100\tAA', 'GENE002\t1\t200\tAG', 'GENE003\t1\t300\tGG'].join(
      '\n',
    );
    expect(detectFormat(content)).toBe('unknown');
  });

  it('returns "unknown" for 3-column tab-separated data (ambiguous)', () => {
    const content = ['rs4477212\t1\tAA', 'rs3094315\t1\tAG'].join('\n');
    expect(detectFormat(content)).toBe('unknown');
  });
});

// ─── 2. Parser — 23andMe ─────────────────────────────────────────────────────
//
// Tests v3/v4/v5 header text differences, specific data format variations,
// indel notation, chromosome handling, and rsID prefix filtering.

describe('Parser — 23andMe', () => {
  // ── v3 format (OmniExpress Plus, ~960K SNPs) ──────────────────────────────

  it('parses v3 format: standard header with build 37 comment', () => {
    // v3 files have 4-column tab-separated format with build number in comments.
    const content = [
      '# This data file generated by 23andMe at: Mon Jan 01 2024',
      '# Below is a tab-separated file of your raw SNP data.',
      '# Build: 37',
      '# rsid\tchromosome\tposition\tgenotype',
      'rs4477212\t1\t82154\tAA',
      'rs3094315\t1\t752566\tAG',
      'rs3131972\t1\t752721\tGG',
    ].join('\n');
    const result = parse23andMe(content);
    expect(result['rs4477212']).toBe('AA');
    expect(result['rs3094315']).toBe('AG');
    expect(result['rs3131972']).toBe('GG');
    expect(Object.keys(result)).toHaveLength(3);
  });

  it('parses v4 format: same structure as v3 but with different SNPs', () => {
    // v4 header is identical in structure to v3.
    const content = [
      '# This data file generated by 23andMe at: Fri Mar 15 2019',
      '# rsid\tchromosome\tposition\tgenotype',
      'rs4477212\t1\t82154\tAA',
      'rs3094315\t1\t752566\tAG',
    ].join('\n');
    const result = parse23andMe(content);
    expect(result['rs4477212']).toBe('AA');
    expect(result['rs3094315']).toBe('AG');
    expect(Object.keys(result)).toHaveLength(2);
  });

  it('parses v5 format: GSA chip marker SNPs present', () => {
    // v5 (GSA chip) marker SNPs: rs548049170, rs13354714, rs2298108.
    const content = [
      '# This data file generated by 23andMe',
      '# rsid\tchromosome\tposition\tgenotype',
      'rs548049170\t1\t100000\tAA',
      'rs13354714\t2\t200000\tCC',
      'rs2298108\t3\t300000\tAG',
      'rs4477212\t1\t82154\tAA',
    ].join('\n');
    const result = parse23andMe(content);
    expect(result['rs548049170']).toBe('AA');
    expect(result['rs13354714']).toBe('CC');
    expect(result['rs2298108']).toBe('AG');
    expect(result['rs4477212']).toBe('AA');
    expect(Object.keys(result)).toHaveLength(4);
  });

  it('parses 23andMe files without any comment header lines', () => {
    // Some exports omit all comment lines. Parser relies on column count heuristic.
    const content = [
      'rs4477212\t1\t82154\tAA',
      'rs3094315\t1\t752566\tAG',
      'rs3131972\t1\t752721\tGG',
    ].join('\n');
    const result = parse23andMe(content);
    expect(result['rs4477212']).toBe('AA');
    expect(result['rs3094315']).toBe('AG');
    expect(result['rs3131972']).toBe('GG');
  });

  it('correctly extracts rsID (col 0), chromosome (col 1), position (col 2), genotype (col 3)', () => {
    // Confirms that column order: rsid, chr, pos, genotype is correctly parsed.
    const content = ['# 23andMe test', 'rs9999999\t22\t51244106\tCT'].join('\n');
    const result = parse23andMe(content);
    // Only the rsID and genotype are stored — chromosome and position are not in GenotypeMap.
    expect(result['rs9999999']).toBe('CT');
  });

  it('skips lines where genotype is "--" (no-call)', () => {
    const content = ['# 23andMe', 'rs4477212\t1\t82154\tAA', 'rs3094315\t1\t752566\t--'].join('\n');
    const result = parse23andMe(content);
    expect(result['rs3094315']).toBeUndefined();
    expect(Object.keys(result)).toHaveLength(1);
  });

  it('skips lines where genotype is empty string (no-call variant)', () => {
    // Some files have trailing tab with no genotype value.
    const content = ['# 23andMe', 'rs4477212\t1\t82154\tAA', 'rs3094315\t1\t752566\t'].join('\n');
    const result = parse23andMe(content);
    expect(result['rs3094315']).toBeUndefined();
  });

  it('handles i-prefixed indel rsIDs (present in 23andMe files)', () => {
    // 23andMe uses "i" prefix for proprietary indel IDs.
    const content = ['# 23andMe', 'i713426\t1\t817186\tCC', 'i3000468\t1\t831489\tTT'].join('\n');
    const result = parse23andMe(content);
    expect(result['i713426']).toBe('CC');
    expect(result['i3000468']).toBe('TT');
  });

  it('handles hemizygous single-character genotypes (X/Y chromosomes in males)', () => {
    // Male X/Y chromosomes produce single-character genotypes (e.g., "A" not "AA").
    const content = [
      '# 23andMe',
      'rs4477212\t1\t82154\tAA',
      'rs700001\tX\t100000\tA',
      'rs700002\tY\t200000\tG',
    ].join('\n');
    const result = parse23andMe(content);
    expect(result['rs700001']).toBe('A');
    expect(result['rs700002']).toBe('G');
  });

  it('handles MT chromosome entries', () => {
    // Mitochondrial DNA uses "MT" chromosome designation in 23andMe.
    const content = ['# 23andMe', 'rs1234\tMT\t12345\tAA'].join('\n');
    const result = parse23andMe(content);
    expect(result['rs1234']).toBe('AA');
  });

  it('skips lines where rsID does not start with rs or i', () => {
    // Lines with non-standard ID prefixes are silently dropped.
    const content = [
      '# 23andMe',
      'rs4477212\t1\t82154\tAA',
      'GENE001\t1\t99999\tCC',
      'VG00001\t1\t88888\tTT',
    ].join('\n');
    const result = parse23andMe(content);
    // Only rs4477212 passes — VG and GENE prefixes are dropped.
    expect(Object.keys(result)).toHaveLength(1);
    expect(result['rs4477212']).toBe('AA');
  });

  it('handles whitespace-padded rsIDs and genotypes (trims correctly)', () => {
    // Some tools export files with extra whitespace in fields.
    const content = ['# 23andMe', 'rs4477212 \t1\t82154\t AA '].join('\n');
    const result = parse23andMe(content);
    expect(result['rs4477212']).toBe('AA');
  });

  it('parses large genotypes like "DDII" (indel notation)', () => {
    // 23andMe v3 encodes indels as D (deletion) / I (insertion) characters.
    // These are valid genotypes and should NOT be skipped.
    const content = [
      '# 23andMe',
      'rs3135027\t1\t900000\tDD',
      'rs3135028\t1\t900001\tII',
      'rs3135029\t1\t900002\tDI',
    ].join('\n');
    const result = parse23andMe(content);
    expect(result['rs3135027']).toBe('DD');
    expect(result['rs3135028']).toBe('II');
    expect(result['rs3135029']).toBe('DI');
  });

  it('throws error when content has only comment lines and no SNP data', () => {
    const content = [
      '# 23andMe raw data file',
      '# This file contains no data',
      '# rsid\tchromosome\tposition\tgenotype',
    ].join('\n');
    expect(() => parse23andMe(content)).toThrow('No valid SNP data found');
  });
});

