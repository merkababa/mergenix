// ─── 10. Parser — getGenotypeStats (extended cases) ──────────────────────────
//
// Supplements the existing getGenotypeStats tests with indel genotype handling
// and larger distribution scenarios.

describe('Parser — getGenotypeStats (extended)', () => {
  it('does not count 4-character indel genotypes as homozygous or heterozygous', () => {
    // Indel genotypes like "DDII" or "AT/G" have length != 2, so they fall into
    // the "else" branch in the classifier and are not counted in either bucket.
    const genotypes = {
      rs1: 'AA', // homozygous (length 2, [0]===[1])
      rs2: 'AT/G', // indel (length 4 — neither counted)
      rs3: 'AG', // heterozygous (length 2, [0]!==[1])
    };
    const stats = getGenotypeStats(genotypes);
    expect(stats.totalSnps).toBe(3);
    expect(stats.homozygousCount).toBe(1);
    expect(stats.heterozygousCount).toBe(1);
    // rs2 (indel) is not counted in either bucket.
  });

  it('counts 3-character genotypes (indel-like) as neither homozygous nor heterozygous', () => {
    const genotypes = { rs1: 'ATG' };
    const stats = getGenotypeStats(genotypes);
    expect(stats.totalSnps).toBe(1);
    expect(stats.homozygousCount).toBe(0);
    expect(stats.heterozygousCount).toBe(0);
  });

  it('correctly builds distribution for a mixed genotype set', () => {
    const genotypes = {
      rs1: 'AA',
      rs2: 'AA',
      rs3: 'AA',
      rs4: 'AG',
      rs5: 'GG',
    };
    const stats = getGenotypeStats(genotypes);
    expect(stats.genotypeDistribution['AA']).toBe(3);
    expect(stats.genotypeDistribution['AG']).toBe(1);
    expect(stats.genotypeDistribution['GG']).toBe(1);
    expect(stats.homozygousCount).toBe(4); // AA×3 + GG×1
    expect(stats.heterozygousCount).toBe(1); // AG×1
  });

  it('handles a single-entry genotype map correctly', () => {
    const genotypes = { rs1: 'CT' };
    const stats = getGenotypeStats(genotypes);
    expect(stats.totalSnps).toBe(1);
    expect(stats.homozygousCount).toBe(0);
    expect(stats.heterozygousCount).toBe(1);
    expect(stats.genotypeDistribution['CT']).toBe(1);
  });
});

