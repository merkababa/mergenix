// ─── 4. Massive Uncompressed Sizes (Very Long Lines) ────────────────────────

describe('Fuzzing — Massive Uncompressed Sizes / Long Lines', () => {
  it(
    '23andMe file with a 1MB comment line → parses without OOM (skips comment)',
    () => {
      // A 1MB comment line should be skipped (starts with '#') without crash
      const oneMbComment = '# ' + 'X'.repeat(1_000_000);
      const content = [
        oneMbComment,
        '# rsid\tchromosome\tposition\tgenotype',
        'rs111\t1\t100\tAA',
        'rs222\t1\t200\tGG',
      ].join('\n');

      // Should parse fine — the 1MB comment line is skipped
      const result = parse23andMe(content);
      expect(Object.keys(result).length).toBe(2);
      expect(result['rs111']).toBe('AA');
    },
    30_000,
  );

  it(
    '23andMe file with a 100KB rsid → skipped or parsed, but no crash',
    () => {
      // A very long rsID that starts with 'rs' but is 100KB
      const longRsid = 'rs' + 'A'.repeat(100_000);
      const content = [
        '# This data file generated by 23andMe',
        '# rsid\tchromosome\tposition\tgenotype',
        `${longRsid}\t1\t100\tAA`, // extremely long rsid
        'rs222\t1\t200\tGG', // valid line — this is the only parseable variant
      ].join('\n');

      // The parser must not crash. The long rsid will either be accepted or rejected.
      // Either outcome is acceptable as long as there is no crash.
      let result: Record<string, string> | null = null;
      let caughtError: Error | null = null;
      try {
        result = parse23andMe(content);
      } catch (err) {
        caughtError = err as Error;
      }

      if (caughtError !== null) {
        expect(caughtError).toBeInstanceOf(Error);
      } else {
        // At minimum, the valid rs222 line should be present
        expect(result).not.toBeNull();
        expect(result!['rs222']).toBe('GG');
      }
    },
    30_000,
  );

  it(
    '23andMe file with 10000 valid lines + 1 extremely long data line → no crash',
    () => {
      const lines: string[] = [
        '# This data file generated by 23andMe',
        '# rsid\tchromosome\tposition\tgenotype',
      ];
      for (let i = 0; i < 9999; i++) {
        lines.push(`rs${1000000 + i}\t1\t${100000 + i}\tAA`);
      }
      // One very long line (100KB data in the genotype field — will be skipped as invalid genotype)
      lines.push(`rs9999999\t1\t999999\t${'A'.repeat(100_000)}`);

      const content = lines.join('\n');
      // Should parse the valid lines without crashing.
      // The parser accepts any non-empty genotype, so the 100KB genotype line
      // may also be parsed (rs9999999 with a 100K-char genotype).
      // We assert: no crash, and at least 9999 lines were parsed.
      const result = parse23andMe(content);
      expect(Object.keys(result).length).toBeGreaterThanOrEqual(9999);
    },
    30_000,
  );

  it(
    'AncestryDNA with a 500KB genotype value → handled without crash',
    () => {
      const content = [
        '#AncestryDNA raw data download',
        '#This file was generated by AncestryDNA',
        'rsid\tchromosome\tposition\tallele1\tallele2',
        `rs111\t1\t100\t${'A'.repeat(500_000)}\tG`, // 500KB allele1
        'rs222\t1\t200\tA\tG', // valid fallback
      ].join('\n');

      // AncestryDNA parser: allele1.toUpperCase().trim() is 500K chars.
      // A 500KB allele1 will be concatenated with G, producing a 500KB+ genotype.
      // The rs222 line will still parse.
      let result: Record<string, string> | null = null;
      let caughtError: Error | null = null;
      try {
        result = parseAncestryDNA(content);
      } catch (err) {
        caughtError = err as Error;
      }

      // Must not crash the process
      if (caughtError !== null) {
        expect(caughtError).toBeInstanceOf(Error);
      } else {
        expect(result).not.toBeNull();
      }
    },
    30_000,
  );
});

