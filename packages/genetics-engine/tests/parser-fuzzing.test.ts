/**
 * Parser / Decompression Fuzzing Tests — Q22 (Stream Q Sprint 3)
 *
 * Tests parser robustness against malicious, malformed, and adversarial input.
 *
 * Security scenarios covered:
 *  1. Zip bomb ratio enforcement (>100:1 ratio aborts)
 *  2. Truncated files (file cut mid-line) → graceful error, not crash
 *  3. Garbage/binary headers → graceful error, not crash
 *  4. Massive uncompressed sizes (very long lines) → handled without OOM
 *  5. Empty file (0 bytes) → empty result or error, not crash
 *  6. Binary file (non-text) → graceful error
 *  7. Null bytes embedded in text → handled
 *  8. Extremely long rsIDs → rejected or truncated
 *  9. 30s timeout enforcement (it.todo — timeout not yet enforced on parse path)
 *
 * These tests use the parser's string-based API directly to avoid needing
 * the full decompression pipeline (which requires browser APIs not present in Node).
 * For decompression ratio tests we test the detectCompression + DecompressionError
 * interfaces directly.
 *
 * All tests assert that the parser returns an error result rather than
 * throwing an uncaught exception. Acceptable outcomes:
 *  - Returns an Error via rejection/throw (caught with try/catch)
 *  - Returns an empty GenotypeMap ({}), never crashes Node
 *
 * Note on Vitest APIs:
 *  - Use `it.todo('description')` for unimplemented/deferred tests (Vitest equivalent of test.fixme)
 *  - Use the timeout number as the third argument to `it()` for slow tests
 */

