// ─── 6. Binary File (Non-Text) ───────────────────────────────────────────────

describe('Fuzzing — Binary File / Non-Text Input', () => {
  it('binary magic bytes (PDF) → detectCompression returns "raw"', () => {
    // PDF starts with %PDF (0x25 0x50 0x44 0x46)
    const pdfHeader = new Uint8Array([0x25, 0x50, 0x44, 0x46, 0x2d, 0x31, 0x2e]);
    const format = detectCompression('report.pdf', pdfHeader);
    expect(format).toBe('raw');
  });

  it('binary-looking string content → detectFormat returns "unknown"', () => {
    // Simulate binary data as a string (non-printable characters)
    let binaryStr = '';
    for (let i = 0; i < 200; i++) {
      binaryStr += String.fromCharCode(i % 256);
    }
    const format = detectFormat(binaryStr);
    expect(format).toBe('unknown');
  });

  it('binary content → parseGeneticFile throws descriptive error (no crash)', () => {
    let binaryStr = '';
    for (let i = 1; i < 200; i++) {
      // Avoid null byte to keep as string
      binaryStr += String.fromCharCode(i);
    }
    expect(() => parseGeneticFile(binaryStr)).toThrow(/unrecognized genetic data format/i);
  });

  it('PNG magic bytes → detectCompression returns "raw"', () => {
    // PNG: 0x89 0x50 0x4E 0x47
    const pngHeader = new Uint8Array([0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a]);
    const format = detectCompression('image.png', pngHeader);
    // PNG magic doesn't match ZIP (0x50 0x4B) or GZIP (0x1F 0x8B)
    expect(format).toBe('raw');
  });

  it('JPEG magic bytes → detectCompression returns "raw"', () => {
    // JPEG: 0xFF 0xD8
    const jpegHeader = new Uint8Array([0xff, 0xd8, 0xff, 0xe0]);
    const format = detectCompression('photo.jpg', jpegHeader);
    expect(format).toBe('raw');
  });
});

