/**
 * Assert that a parse call either throws an Error (expected for invalid input)
 * or resolves gracefully without crashing the process.
 * The returned Error (if any) is also returned for further assertions.
 */
async function expectGracefulError(fn: () => unknown): Promise<Error | null> {
  try {
    const result = await Promise.resolve(fn());
    // If the parser returned something without throwing, that is also acceptable
    // as long as the process did not crash. A valid empty object is acceptable
    // only if there was truly nothing parseable.
    if (result !== undefined && result !== null) {
      return null;
    }
    return null;
  } catch (err) {
    // Must be an Error instance — not an uncaught anything-goes crash
    expect(err).toBeInstanceOf(Error);
    return err as Error;
  }
}

/**
 * Build a string of repeating ASCII garbage (non-genetic-data bytes).
 */
function makeGarbage(length: number): string {
  const chars = '!@#$%^&*()_+=-[]{};:\'"<>?,./~`|\\';
  let result = '';
  for (let i = 0; i < length; i++) {
    result += chars[i % chars.length];
  }
  return result;
}

/**
 * Build a string with null bytes embedded in valid-looking 23andMe content.
 */
function make23andMeWithNulls(): string {
  return [
    '# This data file generated by 23andMe',
    '# rsid\tchromosome\tposition\tgenotype',
    `rs111\t1\t100\tAA`,
    `rs222\t1\t200\tGG`,
    `rs\x00333\t1\t300\tAT`, // null byte in rsid
    `rs444\t1\t400\tCC`,
  ].join('\n');
}

// ─── 1. Zip Bomb Ratio Enforcement ──────────────────────────────────────────

describe('Fuzzing — Zip Bomb Ratio Detection', () => {
  /**
   * The decompression module enforces maxRatio (default 100:1).
   * We test the DecompressionError class and the ratio logic directly since
   * actual browser-based decompression is not available in Node/Vitest.
   */

  it('DecompressionError is constructable with RATIO_EXCEEDED code', () => {
    const err = new DecompressionError(
      'RATIO_EXCEEDED',
      'Compression ratio (101:1) exceeds maximum allowed (100:1).',
    );
    expect(err).toBeInstanceOf(Error);
    expect(err).toBeInstanceOf(DecompressionError);
    expect(err.code).toBe('RATIO_EXCEEDED');
    expect(err.name).toBe('DecompressionError');
    expect(err.message).toContain('101:1');
  });

  it('DecompressionError is constructable with SIZE_EXCEEDED code', () => {
    const err = new DecompressionError(
      'SIZE_EXCEEDED',
      'Decompressed size exceeds maximum.',
    );
    expect(err.code).toBe('SIZE_EXCEEDED');
  });

  it('DecompressionError is constructable with TIMEOUT code', () => {
    const err = new DecompressionError('TIMEOUT', 'Decompression timed out after 30000 ms.');
    expect(err.code).toBe('TIMEOUT');
  });

  it('DecompressionError is constructable with CORRUPT code', () => {
    const err = new DecompressionError('CORRUPT', 'No genetic data file found inside ZIP.');
    expect(err.code).toBe('CORRUPT');
  });

  it('DecompressionError is constructable with UNSUPPORTED_FORMAT code', () => {
    const err = new DecompressionError(
      'UNSUPPORTED_FORMAT',
      'DecompressionStream API is not available.',
    );
    expect(err.code).toBe('UNSUPPORTED_FORMAT');
  });

  /**
   * Simulate ratio check logic in isolation: if originalSize > 0 and
   * decompressedSize / originalSize > maxRatio, a RATIO_EXCEEDED error
   * must be constructable and carry the correct metadata.
   */
  it('ratio check: 101:1 ratio would trigger RATIO_EXCEEDED', () => {
    const originalSize = 1000;
    const decompressedSize = 101000; // 101:1 ratio
    const maxRatio = 100;

    const ratio = decompressedSize / originalSize;
    expect(ratio).toBeGreaterThan(maxRatio);

    const err = new DecompressionError(
      'RATIO_EXCEEDED',
      `Compression ratio (${Math.round(ratio)}:1) exceeds maximum allowed (${maxRatio}:1).`,
    );
    expect(err.code).toBe('RATIO_EXCEEDED');
    expect(err.message).toContain('101:1');
  });

  it('ratio check: 99:1 ratio does NOT trigger RATIO_EXCEEDED', () => {
    const originalSize = 1000;
    const decompressedSize = 99000; // 99:1 ratio
    const maxRatio = 100;

    const ratio = decompressedSize / originalSize;
    expect(ratio).toBeLessThanOrEqual(maxRatio);
  });

  /**
   * Deferred: zip bomb test requires browser environment (DecompressionStream API).
   * The monitoring stream in decompression.ts enforces this in production via
   * createMonitoredStream which checks ratio > maxRatio on each chunk.
   */
  it.todo(
    'zip bomb (>100:1 ratio) aborts decompression and throws RATIO_EXCEEDED [requires browser env]',
  );
});

