    profiles_myheritage = ['healthy', 'carrier', 'mixed', 'affected_some', 'healthy', 'carrier']
    profiles_vcf = ['healthy', 'carrier', 'mixed', 'affected_some', 'mixed']

    # Generate 23andMe files (6 files)
    print("\n=== Generating 23andMe files ===")
