def genotype_to_vcf_gt(genotype, ref, alt):
    """Convert genotype string to VCF GT format."""
    a1, a2 = genotype[0], genotype[1]

    # Determine allele indices (0 = ref, 1 = alt)
    def allele_index(a):
        if a == ref:
            return '0'
        else:
            return '1'

    return f"{allele_index(a1)}/{allele_index(a2)}"

def generate_vcf_file(filepath, diseases, traits, filler_rsids, profile_type, file_seed):
    """Generate a VCF format file."""
    random.seed(file_seed)

    today = datetime.now().strftime("%Y%m%d")

    lines = [
        "##fileformat=VCFv4.2",
        f"##fileDate={today}",
        "##source=TortitSampleGenerator",
        "##reference=GRCh38",
        '##INFO=<ID=NS,Number=1,Type=Integer,Description="Number of Samples With Data">',
        '##FORMAT=<ID=GT,Number=1,Type=String,Description="Genotype">',
        '##FORMAT=<ID=GQ,Number=1,Type=Integer,Description="Genotype Quality">',
        '##FORMAT=<ID=DP,Number=1,Type=Integer,Description="Read Depth">',
        "#CHROM\tPOS\tID\tREF\tALT\tQUAL\tFILTER\tINFO\tFORMAT\tSAMPLE"
    ]

    position = 752566

    # Add all disease SNPs
    for disease in diseases:
        genotype = get_genotype_for_disease(disease, profile_type)
        ref = disease['reference_allele']
        alt = disease['pathogenic_allele']
        gt = genotype_to_vcf_gt(genotype, ref, alt)
        chrom = random.randint(1, 22)
        gq = random.randint(90, 99)
        dp = random.randint(30, 100)
        lines.append(f"{chrom}\t{position}\t{disease['rsid']}\t{ref}\t{alt}\t.\tPASS\tNS=1\tGT:GQ:DP\t{gt}:{gq}:{dp}")
        position += random.randint(100, 500)

    # Add all trait SNPs
    for trait in traits:
        genotype = get_genotype_for_trait(trait, file_seed)
        ref = trait['alleles']['ref']
        alt = trait['alleles']['alt']
        gt = genotype_to_vcf_gt(genotype, ref, alt)
        chrom = trait.get('chromosome', str(random.randint(1, 22)))
        gq = random.randint(90, 99)
        dp = random.randint(30, 100)
        lines.append(f"{chrom}\t{position}\t{trait['rsid']}\t{ref}\t{alt}\t.\tPASS\tNS=1\tGT:GQ:DP\t{gt}:{gq}:{dp}")
        position += random.randint(100, 500)

    # Add filler SNPs
    for rsid in filler_rsids:
        genotype = get_filler_genotype()
        ref = genotype[0]
        alt = genotype[1] if genotype[1] != genotype[0] else random.choice([a for a in 'ACGT' if a != ref])
        gt = genotype_to_vcf_gt(genotype, ref, alt)
        chrom = random.randint(1, 22)
        gq = random.randint(90, 99)
        dp = random.randint(30, 100)
        lines.append(f"{chrom}\t{position}\t{rsid}\t{ref}\t{alt}\t.\tPASS\tNS=1\tGT:GQ:DP\t{gt}:{gq}:{dp}")
        position += random.randint(100, 500)

    with open(filepath, 'w', encoding='utf-8') as f:
        f.write('\n'.join(lines))

    print(f"Generated: {filepath}")

