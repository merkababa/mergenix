def load_json(filepath):
    with open(filepath, 'r', encoding='utf-8') as f:
        return json.load(f)

def generate_filler_rsids(count=100):
    """Generate common filler rsIDs that look realistic."""
    filler = []
    # Common rsIDs that are often in DTC tests but not in our panels
    common_prefixes = [1000, 2000, 3000, 4000, 5000, 6000, 7000, 8000, 9000]
    for i in range(count):
        prefix = random.choice(common_prefixes)
        filler.append(f"rs{prefix + random.randint(1, 999)}")
    return list(set(filler))  # Remove duplicates

def get_genotype_for_disease(disease, profile_type):
    """
    Generate genotype based on profile type.
    profile_type: 'healthy', 'carrier', 'mixed', 'affected_some'
    """
    ref = disease['reference_allele']
    path = disease['pathogenic_allele']

    if profile_type == 'healthy':
        # 90% homozygous reference, 10% carrier
        if random.random() < 0.90:
            return ref + ref
        else:
            return ref + path if random.random() < 0.5 else path + ref
    elif profile_type == 'carrier':
        # 60% carrier, 30% homozygous reference, 10% affected
        r = random.random()
        if r < 0.60:
            return ref + path if random.random() < 0.5 else path + ref
        elif r < 0.90:
            return ref + ref
        else:
            return path + path
    elif profile_type == 'affected_some':
        # 20% affected, 40% carrier, 40% reference
        r = random.random()
        if r < 0.20:
            return path + path
        elif r < 0.60:
            return ref + path if random.random() < 0.5 else path + ref
        else:
            return ref + ref
    else:  # mixed
        # Random distribution
        r = random.random()
        if r < 0.70:
            return ref + ref
        elif r < 0.95:
            return ref + path if random.random() < 0.5 else path + ref
        else:
            return path + path

def get_genotype_for_trait(trait, variation_seed):
    """Generate genotype from available phenotype_map genotypes with variation."""
    available_genotypes = list(trait['phenotype_map'].keys())

    # Use variation_seed to get different distributions per file
    random.seed(variation_seed + hash(trait['rsid']))
    genotype = random.choice(available_genotypes)
    random.seed()  # Reset seed

    return genotype

def get_filler_genotype():
    """Generate a random genotype for filler SNPs."""
    alleles = ['A', 'C', 'G', 'T']
    a1 = random.choice(alleles)
    a2 = random.choice(alleles)
    return a1 + a2

def generate_23andme_file(filepath, diseases, traits, filler_rsids, profile_type, file_seed):
    """Generate a 23andMe format file."""
    random.seed(file_seed)

    lines = [
        "# This data file generated by 23andMe at: " + datetime.now().strftime("%a %b %d %H:%M:%S %Y"),
        "#",
        "# This file contains raw genotype data, including data that is not used in 23andMe reports.",
        "# This data has undergone a general quality review however only a subset of markers have been ",
        "# individually validated for accuracy. As such, this data is suitable only for research, ",
        "# educational, and informational use and not for medical or other use.",
        "#",
        "# Below is a text version of your data. Fields are TAB-separated",
        "# Each line corresponds to a single SNP. For each SNP, we provide its identifier ",
        "# (an rsid or an internal id), its location on the reference human genome, and the ",
        "# genotype call oriented with respect to the plus strand on the human reference sequence.",
        "#",
        "# rsid\tchromosome\tposition\tgenotype"
    ]

    position = 752566

    # Add all disease SNPs
    for disease in diseases:
        genotype = get_genotype_for_disease(disease, profile_type)
        chrom = random.randint(1, 22)
        lines.append(f"{disease['rsid']}\t{chrom}\t{position}\t{genotype}")
        position += random.randint(100, 500)

    # Add all trait SNPs
    for trait in traits:
        genotype = get_genotype_for_trait(trait, file_seed)
        chrom = trait.get('chromosome', str(random.randint(1, 22)))
        lines.append(f"{trait['rsid']}\t{chrom}\t{position}\t{genotype}")
        position += random.randint(100, 500)

    # Add filler SNPs
    for rsid in filler_rsids:
        genotype = get_filler_genotype()
        chrom = random.randint(1, 22)
        lines.append(f"{rsid}\t{chrom}\t{position}\t{genotype}")
        position += random.randint(100, 500)

    with open(filepath, 'w', encoding='utf-8') as f:
        f.write('\n'.join(lines))

    print(f"Generated: {filepath}")

